<p>Alpert Law Firm recognizes the importance of privacy and the sensitivity of personal information. Lawyers have a professional obligation to keep confidential all information received within a lawyer-client relationship. Alpert Law Firm is committed to protecting any personal information it holds. This Privacy Policy outlines how Alpert Law Firm manages your personal information and safeguards your privacy.</p>

<p><strong>YOUR PRIVACY RIGHTS</strong></p>

<p>From January 1, 2004, all businesses engaged in commercial activities must comply with the Personal Information Protection and Electronic Documents Act, and the Canadian Standards Association Model Code for the Protection of Personal Information, which it incorporates. These obligations extend to lawyers and law firms, including Alpert Law Firm. The Act gives you rights concerning the privacy of your personal information.</p>

<p>Alpert Law Firm is responsible for the personal information it collects and holds. To ensure this accountability, Alpert Law Firm has developed this policy, and trained its staff about these policies and practices.</p>

<p><strong>WHY DOES ALPERT LAW FIRM NEED PERSONAL INFORMATION</strong></p>

<p>Alpert Law Firm provides legal services and products to a wide range of clients. In doing so, it produces direct marketing materials concerning its services and developments in the law.

<p><strong>WHAT PERSONAL INFORMATION DOES ALPERT LAW FIRM COLLECT?</strong></p>

<p>Personal information is any information that identifies you, or by which your identity could be deduced.</p>

<p>If Alpert Law Firm did not collect and use your personal information it could not provide you with legal services.</p>

<p><strong>HOW DOES ALPERT LAW FIRM COLLECT YOUR PERSONAL INFORMATION?</strong></p>

<p>Alpert Law Firm collects information only by lawful and fair means and not in an unreasonably intrusive way. Wherever possible Alpert Law Firm collects your personal information directly from you, at the start of a retainer and in the course of its representation.</p>

<p>Sometimes Alpert Law Firm may obtain information about you from other sources: for example,</p>
<ul>
  <li>your insurance company;</li>
  <li>your real estate agent in a property transaction;</li>
  <li>from a government agency or registry;</li>
  <li>your employer, if we are acting for you, at its request;</li>
  <li>your accountant.</li>
</ul>
<p><strong>CONSENT</strong></p>

<p>In most cases, Alpert Law Firm shall ask you to specifically consent, if it collects, uses, or discloses your personal information. Normally, Alpert Law Firm asks for your consent in writing, but in some circumstances, it may accept your oral consent. Sometimes, your consent may be implied through your conduct with Alpert Law Firm.</p>

<p><strong>USE OF YOUR INFORMATION</strong></p>

<p>Alpert Law Firm uses your personal information to provide legal advice and services to you, to administer client databases and to include you in any direct marketing activities. If you tell Alpert Law Firm that you no longer wish to receive information about its services, or about new developments in the law, it will not send any further material.</p>

<p>Alpert Law Firm does not disclose your personal information to any third party to enable them to market their products and services. For example, Alpert Law Firm does not provide its client mailing lists to other law firms.</p>

<p><strong>DISCLOSURE OF YOUR PERSONAL INFORMATION</strong></p>

<p>Under certain circumstances, Alpert Law Firm will disclose your personal information:</p>
<ul>
  <li>when it is required or authorized by law to do so, for example if a court issues a subpoena;</li>
  <li>when you have consented to the disclosure;</li>
  <li>when the legal services it is providing to you requires it to give your information to third parties your consent will be implied, unless you tell us otherwise;</li>
  <li>where it is necessary to establish or collect fees</li>
  <li>if it engages a third party to provide administrative services to it (like computer back-up services or archival file storage) and the third party is bound by Alpert Law Firm’s privacy policy;</li>
  <li>if it engages expert witnesses on your behalf;</li>
  <li>if it retains other law firms in other jurisdictions, on your behalf;</li>
  <li>if the information is already publicly known.</li>
</ul>

<p><strong>UPDATING YOUR INFORMATION</strong></p>

<p>Since Alpert Law Firm uses your personal information to provide legal services to you, it is important that the information be accurate and up-to-date.</p>

<p>If during the course of the retainer, any of your information changes, please inform us so that we can make any necessary changes.</p>

<p><strong>IS YOUR PERSONAL INFORMATION SECURE?</strong></p>

<p>Alpert Law Firm takes all reasonable precautions to ensure that your personal information is kept safe from loss, unauthorized access, modifications or disclosure.</p>

<p>Among the steps taken to protect your information are:</p>
<ul>
  <li>premises security;</li>
  <li>restricted file access to personal information;</li>
  <li>deploying technological safeguards to attempt to prevent hacking or unauthorized computer access;</li>
  <li>internal password and security policies.</li>
</ul>

<p><strong>ACCESS TO YOUR PERSONAL INFORMATION</strong></p>

<p>You may ask for access to any personal information Alpert Law Firm holds about you.</p>

<p>Summary information is available on request. More detailed requests which require archive or other retrieval costs may be subject to Alpert Law Firm’s normal professional and disbursement fees.</p>

<p><strong>CORRECTING ERRORS</strong></p>

<p>If Alpert Law Firm holds information about you and you can establish that it is not accurate, complete and up-to-date, Alpert Law Firm will take reasonable steps to correct it.</p>

<p><strong>CAN YOU BE DENIED ACCESS TO YOUR PERSONAL INFORMATION</strong></p>

<p>Your rights to access your personal information are not absolute.</p>

<p>Alpert Law Firm may deny access when:</p>

<ul>
  <li>denial of access is required or authorized by law (for example, when a record containing personal information about you is subject to a claim of legal professional privilege by one of its clients);</li>
  <li>information relates to existing or anticipated legal proceedings against you;</li>
  <li>when granting you access would have an unreasonable impact on other people’s privacy;</li>
  when to do so would prejudice negotiations with you;</li>
  <li>to protect Alpert Law Firm’s rights and property;</li>
  <li>where the request is frivolous or vexatious.</li>
</ul>

<p>If Alpert Law Firm denies your request for access to, or refuses a request to correct information, it shall explain why.</p>

<p>Alpert Law Firm does not use your Social Insurance Number as a way of identifying or organizing the information it holds upon you.</p>

<p><strong>CAN YOU REQUEST ANONYMITY</strong></p>

<p>Whenever it is legal and practicable, Alpert Law Firm may offer the opportunity to deal with general inquiries without providing your name (for example, by accessing general information on its website). The Proceeds of Crime (Money Laundering) and Terrorist Financing Act requires Alpert Law Firm to confirm the identity of all new clients. It may also require Alpert Law Firm to disclose information to FINTRAC in relation to certain large cash transactions.</p>

<p><strong>CREDIT BUREAUS</strong></p>

<p>To help us make credit decisions about clients, prevent fraud, check the identity of new clients and prevent money-laundering, Alpert Law Firm may on occasion, request information about you from the files of consumer reporting agencies.</p>

<p><strong>COMMUNICATING WITH ALPERT LAW FIRM</strong></p>

<p>You should be aware that e-mail is not a 100% secure medium, and you should be aware of this when contacting Alpert Law Firm to send personal or confidential information.</p>

<p><strong>CHANGES TO THIS PRIVACY POLICY</strong></p>

<p>Since Alpert Law Firm regularly reviews all of its policies and procedures, it may change its Privacy Policy from time to time.</p>

<p><strong>REQUESTS FOR ACCESS</strong></p>

